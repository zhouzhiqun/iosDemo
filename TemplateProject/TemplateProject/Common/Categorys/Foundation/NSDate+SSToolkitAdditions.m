+ (NSInteger)ss_daysOnYear:(NSInteger)year month:(NSInteger)month {
    
    //å¤æ­æ¯å¦é°å¹´
    BOOL isLeap = NO;
    if ((year % 400 == 0) || ((year % 100 != 0) && (year % 4 == 0)) ) {
        isLeap = YES;
    }
    
    NSInteger days = 30;
    switch (month) {
        case 1:
        case 3:
        case 5:
        case 7:
        case 8:
        case 10:
        case 12:
        {
            //å¤§æ
            days = 31;
            break;
        }
        case 2:
        {
            if (isLeap) {
                days = 29;
            } else {
                days = 28;
            }
            break;
        }
        default:
            days = 30;
            break;
    }
    return days;
}




